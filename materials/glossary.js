  function show(el) {
    var t = TERMS[+el.getAttribute('data-g')];
    if (!t) return;
    tip.innerHTML = '';
    var b = document.createElement('b');
    b.textContent = t[0];
    var d = document.createElement('div');
    d.textContent = t[2];
    tip.appendChild(b);
    tip.appendChild(d);
    var hint = document.createElement('i');
    hint.textContent = '點按名詞可開啟詳細說明欄 ▸';
    tip.appendChild(hint);
    tip.classList.add('show');
    cur = el;
    place(el);
  }

  function place(el) {
    var r = el.getBoundingClientRect();
    tip.style.left = '0px';
    tip.style.top = '0px';
    var tw = tip.offsetWidth, th = tip.offsetHeight;
    var x = r.left + r.width / 2 - tw / 2;
    x = Math.max(8, Math.min(x, window.innerWidth - tw - 8));
    var y = r.top - th - 10;
    if (y < 8) y = r.bottom + 10;
    tip.style.left = x + 'px';
    tip.style.top = y + 'px';
  }

  function hide() {
    if (tip) tip.classList.remove('show');
    cur = null;
  }

  function bindEvents() {
    document.addEventListener('mouseover', function (e) {
      var el = e.target.closest && e.target.closest('.gl');
      if (el) show(el);
      else if (cur) hide();
    });
    document.addEventListener('focusin', function (e) {
      var el = e.target.closest && e.target.closest('.gl');
      if (el) show(el);
    });
    document.addEventListener('focusout', function (e) {
      if (e.target.closest && e.target.closest('.gl')) hide();
    });
    document.addEventListener('click', function (e) { // 點按：開啟名詞說明欄
      var el = e.target.closest && e.target.closest('.gl');
      if (el) { hide(); openPanel(+el.getAttribute('data-g')); }
    });
    document.addEventListener('keydown', function (e) {
      if (e.key === 'Escape') { hide(); closePanel(); }
    });
    window.addEventListener('scroll', function () { if (cur) place(cur); }, { passive: true });
    window.addEventListener('resize', function () { if (cur) place(cur); });
  }

